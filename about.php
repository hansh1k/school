                <div class="about-history-bg">
                    <p><strong>История и традиции учреждения образования</strong></p>
                    <p>История школы берёт начало в далёком 1939 году, когда началось строительство здания. К июню 1941 года здание было готово, но открытию школы помешала война.</p>
                    <p>Во время войны здесь был пересыльный пункт для военнопленных. Отсюда людей отправляли в Германию. Когда в 1943 году фашисты начали терпеть одно за другим поражения, то они организовали в школе госпиталь. После освобождения города Мозыря в январе 1944 года в школьном здании был госпиталь для воинов Советской Армии.</p>
                    <p>По данным государственного архива г. Мозыря на основании решения полесского областного исполкома депутатов трудящихся в целях быстрейшего возобновления работы школ по городу было принято решение Городского Совета депутатов трудящихся от 04.02.1944 года о подготовке и открытию школ по г.Мозырю. Согласно этому решению был установлен срок начала работы средней русской школы №7 к 05.02.1944 года. Сначала учащиеся семилетней школы №7 занимались в здании школы №8. В сентябре 1945 года здание было возвращено к школе. Силами учителей и учащихся сделали ремонт. Парт в школе не было, учились за столами. Писали на газетах, старых книгах, так как не было бумаги. На класс было по 2 – 3 учебника. В школе было очень холодно. За дровами ездили в Наровлю и Романовку. В 1946 году в школе был проведён свет. В нормальных условиях школа начала работать в 1948 году.</p>
